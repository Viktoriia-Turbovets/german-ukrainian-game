// ====== Наступне слово ======
function nextWord() {
  clearInterval(timer);
  timeLeft = 15;
  updateTimerBar();

  const categoryWords = categories[currentCategory];
  if (!Array.isArray(categoryWords) || categoryWords.length === 0) {
    wordCard.textContent = 'Немає слів у цій категорії';
    optionsContainer.innerHTML = '';
    return;
  }

  let available = categoryWords.filter(w => !usedWords.includes(w));
  if (available.length === 0) {
    usedWords = [];
    available = categoryWords.slice();
  }

  currentWord = available[Math.floor(Math.random() * available.length)];
  usedWords.push(currentWord);

  // Відображаємо слово (питання)
  wordCard.textContent = translationDirection === 'de-to-ua' ? currentWord.de : currentWord.ua;

  // Якщо показано німецьке слово — озвучимо його
  if (translationDirection === 'de-to-ua') speakWord(currentWord.de);

  generateOptions();
  updateProgressBar();

  // Запускаємо таймер
  timer = setInterval(() => {
    timeLeft--;
    updateTimerBar();
    if (timeLeft <= 0) {
      clearInterval(timer);
      handleTimeout();
    }
  }, 1000);
}

