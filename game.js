// ====== Допоміжні функції ======
function safePlay(audioEl) {
  if (!audioEl) return;
  try {
    audioEl.currentTime = 0;
    audioEl.play().catch(()=>{ /* ігноруємо помилку автозапуску */ });
  } catch (e) {}
}

function shuffleArray(arr) {
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
}

// ====== Старт / кінець гри ======
function startNewGame() {
  score = 0;
  combo = 0;
  usedWords = [];
  scoreEl.textContent = `Рахунок: ${score}`;
  comboEl.textContent = `Серія правильних відповідей: ${combo}`;
  finalResult.textContent = '';
  feedbackEl.textContent = '';
  nextWord();
}

function endGame() {
  clearInterval(timer);
  finalResult.textContent = `Гра завершена! Ваш рахунок: ${score}`;
  if (score > highscore) {
    highscore = score;
    highscoreEl.textContent = highscore;
  }
}

